        'retry_count', int32(0), ...
        'task_step_id', int32(0), ...
        'human_robot_distance', 1.0);
end

function sim = updateInteraction(sim, schedule, config)
    separation = norm(sim.human.pos_xy - sim.robot.pos_xy);
    human_in_shared = norm(sim.human.pos_xy - config.workspace.shared_zone_center_xy) <= config.workspace.shared_zone_radius_m;
    robot_in_shared = norm(sim.robot.pos_xy - config.workspace.shared_zone_center_xy) <= config.workspace.shared_zone_radius_m;
    simultaneous = human_in_shared && robot_in_shared;
    unsafe_close = separation < config.workspace.hard_safety_radius_m;

    sim.interaction.separation_m = separation;
    sim.interaction.human_in_shared_zone = human_in_shared;
    sim.interaction.robot_in_shared_zone = robot_in_shared;
    sim.interaction.simultaneous_shared_zone = simultaneous;
    sim.interaction.min_separation_m = min(sim.interaction.min_separation_m, separation);

    if simultaneous && ~sim.interaction.previous_simultaneous_shared_zone
        sim.interaction.overlap_event_count = sim.interaction.overlap_event_count + 1;
    end
    if unsafe_close && ~sim.interaction.previous_unsafe_close
        sim.interaction.unsafe_close_call_count = sim.interaction.unsafe_close_call_count + 1;
    end

    sim.interaction.previous_simultaneous_shared_zone = simultaneous;
    sim.interaction.previous_unsafe_close = unsafe_close;

    threshold = min(schedule.crowding_sensitivity_threshold_m, config.human.crowding_mild_threshold_m);
    if separation < threshold
        sim.human.crowding_frames_mild = sim.human.crowding_frames_mild + 1;
    else
        sim.human.crowding_frames_mild = 0;
    end

    if separation < config.human.crowding_strong_threshold_m
        sim.human.crowding_frames_strong = sim.human.crowding_frames_strong + 1;
    else
        sim.human.crowding_frames_strong = 0;
    end

    if separation > 0.25 && ~robot_in_shared
        sim.human.stable_release_frames = sim.human.stable_release_frames + 1;
    else
        sim.human.stable_release_frames = 0;
    end
end

function row = buildFrameRow(sim, schedule, features, prediction)
    row = initialFrameStruct();
    row.scenario_name = sim.scenario_name;
    row.seed = schedule.seed;
    row.policy_name = sim.policy_name;
    row.backend = sim.backend;
    row.frame_idx = sim.frame_idx;
    row.time_sec = sim.time_sec;
    row.human_state = sim.human.state;
    row.human_pos_x = sim.human.pos_xy(1);
    row.human_pos_y = sim.human.pos_xy(2);
    row.robot_pos_x = sim.robot.pos_xy(1);
    row.robot_pos_y = sim.robot.pos_xy(2);
    row.separation_m = sim.interaction.separation_m;
    row.human_in_shared_zone = double(sim.interaction.human_in_shared_zone);
    row.robot_in_shared_zone = double(sim.interaction.robot_in_shared_zone);
    row.simultaneous_shared_zone = double(sim.interaction.simultaneous_shared_zone);
    row.mean_hand_speed = features.mean_hand_speed;
    row.pause_ratio = features.pause_ratio;
    row.progress_delta = features.progress_delta;
    row.reversal_count = double(features.reversal_count);
    row.retry_count = double(features.retry_count);
    row.task_step_id = double(features.task_step_id);
    row.human_robot_distance = features.human_robot_distance;
    row.predicted_state = prediction.state;
    row.future_hesitation_prob = prediction.future_hesitation_prob;
    row.future_correction_prob = prediction.future_correction_prob;
    row.prediction_confidence = prediction.confidence;
    row.robot_mode = sim.robot.mode;
    row.robot_speed_scale = sim.robot.speed_scale_cmd;
    row.policy_intervention_active = double(strcmp(sim.robot.mode, 'hold') || strcmp(sim.robot.mode, 'slow'));
    row.emergency_active = double(strcmp(sim.robot.mode, 'emergency_stop'));
    row.human_speed = norm(sim.human.vel_xy);
    row.human_progress_01 = sim.human.progress_01;
end

