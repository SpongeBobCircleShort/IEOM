function [episode_summary, frame_log] = runABEpisode(policy_name, schedule, config)
% runABEpisode: Run one deterministic A/B episode and return frame log + summary.

    if strcmp(config.backend, 'python_bridge')
        cli_handle = HesitationModelCLI(config.python.model_root_dir);
        cli_handle.python_cmd = config.python.python_cmd;
    else
        cli_handle = [];
    end

    sim = initializeSimulation(policy_name, schedule, config);
    frame_log = repmat(initialFrameStruct(), config.max_frames, 1);
    prediction = defaultPrediction(config);
    hesitation_onset_time = NaN;
    first_intervention_time = NaN;

    for frame_idx = 1:config.max_frames
        sim.frame_idx = frame_idx;
        sim.time_sec = (frame_idx - 1) * config.dt_sec;

        sim = updateInteraction(sim, schedule, config);
        [sim.human, event_flags] = stepHumanStateMachine( ...
            sim.human, sim.robot, sim.interaction, schedule, config, frame_idx);
        sim = updateInteraction(sim, schedule, config);
        [features, sim.buffers] = extractRollingFeatures2D( ...
            sim.buffers, sim.human, sim.robot, sim.interaction, event_flags.task_restart, config);

        if frame_idx >= config.warmup_frames && mod(frame_idx, config.inference_stride_frames) == 0
            if strcmp(config.backend, 'python_bridge')
                [prediction, cli_handle] = inferHesitationPythonBridge(features, config, cli_handle);
            else
                prediction = inferHesitationHeuristic(features, config);
            end
            sim.inference.last_prediction = prediction;
            sim.inference.last_features = features;
            sim.inference.last_update_frame = frame_idx;
        else
            prediction = sim.inference.last_prediction;
            features = sim.inference.last_features;
        end

        if isnan(hesitation_onset_time) && ...
                (strcmp(sim.human.state, 'mild_hesitation') || strcmp(sim.human.state, 'strong_hesitation') || strcmp(sim.human.state, 'correction_rework'))
            hesitation_onset_time = sim.time_sec;
        end

        sim.robot = stepRobotPolicy(sim.robot, sim.human, sim.interaction, prediction, policy_name, schedule, config, frame_idx);
        sim = updateInteraction(sim, schedule, config);

        if strcmp(policy_name, 'B') && isnan(first_intervention_time) && ...
                (strcmp(sim.robot.mode, 'hold') || strcmp(sim.robot.mode, 'slow')) && ~isnan(hesitation_onset_time)
            first_intervention_time = sim.time_sec;
        end

        frame_log(frame_idx) = buildFrameRow(sim, schedule, features, prediction);

        if strcmp(sim.human.state, 'done') && sim.robot.completed
            frame_log = frame_log(1:frame_idx);
            break;
        end
    end

    if numel(frame_log) > config.max_frames
        frame_log = frame_log(1:config.max_frames);
    end

    episode_summary = computeABEpisodeMetrics(frame_log, schedule, config);
    episode_summary.policy_name = policy_name;
    episode_summary.backend = config.backend;
    episode_summary.response_latency_sec = NaN;
    if ~isnan(hesitation_onset_time) && ~isnan(first_intervention_time)
        episode_summary.response_latency_sec = max(first_intervention_time - hesitation_onset_time, 0.0);
    end
end

function sim = initializeSimulation(policy_name, schedule, config)
    sim = struct( ...
        'dt_sec', config.dt_sec, ...
        'frame_rate_hz', config.frame_rate_hz, ...
        'max_frames', config.max_frames, ...
        'frame_idx', 0, ...
        'time_sec', 0.0, ...
        'seed', schedule.seed, ...
        'scenario_name', schedule.scenario_name, ...
        'policy_name', policy_name, ...
        'backend', config.backend, ...
        'workspace', config.workspace, ...
        'human', initializeHuman(config), ...
        'robot', initializeRobot(schedule, config), ...
        'interaction', initializeInteraction(), ...
        'buffers', initializeBuffers(), ...
        'inference', initializeInference(config));
end

