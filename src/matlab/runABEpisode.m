function pos_xy = clampToBounds(pos_xy, bounds_xy)
    pos_xy(1) = min(max(pos_xy(1), bounds_xy(1, 1)), bounds_xy(1, 2));
    pos_xy(2) = min(max(pos_xy(2), bounds_xy(2, 1)), bounds_xy(2, 2));
end
