    config.python = struct( ...
        'model_root_dir', repo_root, ...
        'python_cmd', 'python3');

    config = applyNameValueOverrides(config, varargin{:});
end

function config = applyNameValueOverrides(config, varargin)
    if mod(numel(varargin), 2) ~= 0
        error('buildABConfig requires name/value overrides.');
    end

    for idx = 1:2:numel(varargin)
        name = varargin{idx};
        value = varargin{idx + 1};
        if ~ischar(name) && ~isstring(name)
            error('Override keys must be char or string.');
        end
        config.(char(name)) = value;
    end
end
